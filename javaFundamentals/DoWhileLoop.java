package javaFundamentals;

public class DoWhileLoop {

	public static void main(String[] args) {
		String hello[] = {"Manuel","Cadalig","Pecaoco","III"};
		int i = 0;
		do {
			
			System.out.println(hello[i]);
			i++;
		}while (i<hello.length);
		
		//System.out.println();

	}

}
